def normalize_label(label: str) -> list[tuple[str, str]]:
    """Return list of (iso, display_name) for a nationality label."""
    label = label.strip()
    if not label:
        return []

    if label in NATIONALITY_TO_ISO:
        iso, name = NATIONALITY_TO_ISO[label]
        return [(iso, name)]

    # Try splitting compound "X and Y"
    if " and " in label.lower():
        parts = [p.strip() for p in label.split(" and ") if p.strip()]
        result: list[tuple[str, str]] = []
        for part in parts:
            result.extend(normalize_label(part))
        return _dedupe(result)

    # Try suffix stripping: "American-born", "Argentine-American"
    for suffix in ("-American", "-Canadian", "-born"):
        if suffix in label:
            base = label.replace(suffix, "").strip()
            if base in NATIONALITY_TO_ISO:
                return [NATIONALITY_TO_ISO[base]]

    # Fuzzy: match keys that start with label or label starts with key
    for key, val in NATIONALITY_TO_ISO.items():
        if label.lower() == key.lower():
            return [val]
        if label.lower().startswith(key.lower()) and len(key) > 4:
            return [val]

    return []


def extract_birthplace(block: str) -> list[tuple[str, str]]:
    """Extract birthplace country from 'born Germany' patterns."""
    import re

    match = re.search(r"born\s+([A-Za-z\s.]+)", block, re.IGNORECASE)
    if not match:
        return []
    place = match.group(1).strip().lower().rstrip(".")
    for key, val in BIRTHPLACE_TO_ISO.items():
        if key in place or place.startswith(key):
            return [val]
    return []


def _dedupe(items: list[tuple[str, str]]) -> list[tuple[str, str]]:
    seen: set[str] = set()
    out: list[tuple[str, str]] = []
    for iso, name in items:
        if iso not in seen:
            seen.add(iso)
            out.append((iso, name))
    return out


def get_centroid(iso: str) -> tuple[float, float]:
    return COUNTRY_CENTROIDS.get(iso, (0.0, 0.0))
